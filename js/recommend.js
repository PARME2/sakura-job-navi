const demoName = "山田 太郎";
const demoStrengthIds = [1, 2];

// --- ユーティリティ ---
function getVisionKey(strengthIds) {
  const sorted = [...strengthIds].sort((a, b) => a - b);
  return sorted.join(',');
}

function getVision(visions, strengthIds) {
  const key = getVisionKey(strengthIds);
  if (visions[key]) return visions[key];
  if (visions[String(strengthIds[0])]) return visions[String(strengthIds[0])];
  return "あなたの強みを活かして、この分野で着実に成長し活躍できます。";
}

// --- UI制御 ---
function startAnalysis(method) {
  document.getElementById('recommend-input').style.display = 'none';
  document.getElementById('recommend-loading').style.display = 'block';

  const statusEl = document.getElementById('loading-status');
  const voiceMessages = [
    "音声データを受信中...", "会話内容を解析中...", "あなたの強みを抽出中...",
    "職種との適性を分析中...", "さくら市の企業とマッチング中...", "活躍像を生成中...", "結果をまとめています..."
  ];
  const chatMessages = [
    "チャット内容を解析中...", "回答を分析中...", "あなたの強みを抽出中...",
    "職種との適性を分析中...", "さくら市の企業とマッチング中...", "活躍像を生成中...", "結果をまとめています..."
  ];
  const messages = method === 'voice' ? voiceMessages : chatMessages;
  statusEl.textContent = messages[0];

  let msgIndex = 0;
  const msgInterval = setInterval(() => {
    msgIndex++;
    if (msgIndex < messages.length) statusEl.textContent = messages[msgIndex];
  }, 500);

  setTimeout(() => {
    clearInterval(msgInterval);
    const userStrengths = strengthsDB.filter(s => demoStrengthIds.includes(s.id));
    renderResults(demoName, userStrengths, demoStrengthIds);

    document.getElementById('recommend-loading').style.display = 'none';
    document.getElementById('recommend-results').style.display = 'block';
    if (typeof lucide !== 'undefined') lucide.createIcons();
    document.querySelectorAll('#recommend-results .fade-up').forEach(el => el.classList.add('visible'));
    window.scrollTo({ top: 0, behavior: 'smooth' });
  }, 3600);
}

function renderResults(name, strengths, strengthIds) {
  document.getElementById('result-name').textContent = name;

  document.getElementById('strengths-summary').innerHTML = `
    <div class="strength-summary-card">
      <p>あなたは、周囲との信頼関係を築きながら、目標に向かってコツコツと努力を続けられる人です。<br>
      <strong>${strengths[0].title}</strong>と<strong>${strengths[1].title}</strong>を兼ね備えており、
      ${strengths[0].workStyle}。</p>
    </div>
  `;

  document.getElementById('strengths-grid').innerHTML = strengths.map(s => `
    <div class="strength-card" style="border-left: 4px solid ${s.color};">
      <div class="strength-header">
        <div class="strength-icon" style="background: ${s.color}15; color: ${s.color};">
          <i data-lucide="${s.icon}" class="lucide"></i>
        </div>
        <div>
          <h4 class="strength-title">${s.title}</h4>
          <p class="strength-subtitle">${s.subtitle}</p>
        </div>
      </div>
      <p class="strength-desc">${s.detail}</p>
    </div>
  `).join('');

  // 職種マッチング
  const jobsSorted = jobsDB.map(job => {
    const overlap = job.strengths.filter(s => strengthIds.includes(s)).length;
    return { ...job, overlap };
  }).sort((a, b) => b.overlap - a.overlap);

  document.getElementById('job-matching-grid').innerHTML = jobsSorted.map(j => `
    <div class="vision-card${j.overlap > 0 ? ' vision-card-match' : ''}">
      <div class="vision-card-header">
        <div class="vision-card-icon"><i data-lucide="${j.icon}" class="lucide"></i></div>
        <div class="vision-card-title-wrap">
          <h4 class="vision-card-title">${j.title}</h4>
          <div class="vision-card-jobs">${j.jobs.join(' / ')}</div>
        </div>
        ${j.overlap > 0 ? '<div class="vision-match-badge"><i data-lucide="sparkles" class="lucide"></i> 適性あり</div>' : ''}
      </div>
      <div class="vision-card-body">
        <div class="vision-label"><i data-lucide="user-check" class="lucide"></i> あなたの活躍像</div>
        <p class="vision-text">${getVision(j.visions, strengthIds)}</p>
      </div>
      <div class="vision-card-tags">
        ${j.labels.map(l => `<span class="vision-tag">${l}</span>`).join('')}
      </div>
    </div>
  `).join('');

  // 企業マッチング
  document.getElementById('company-matching-grid').innerHTML = companiesDB.map(c => {
    const overlap = c.matchStrengths.filter(s => strengthIds.includes(s)).length;
    return `
    <div class="vision-card vision-card-company${overlap > 0 ? ' vision-card-match' : ''}">
      <div class="vision-company-header">
        <div class="vision-company-photo">
          <img src="${c.image}" alt="${c.name}">
          <span class="vision-company-label">${c.label}</span>
        </div>
        <div class="vision-company-info">
          <h4 class="vision-card-title">${c.name}</h4>
          <span class="vision-company-meta">${c.industry} / ${c.jobType}</span>
          <p class="vision-company-desc">${c.description}</p>
        </div>
        ${overlap > 0 ? '<div class="vision-match-badge"><i data-lucide="sparkles" class="lucide"></i> 適性あり</div>' : ''}
      </div>
      <div class="vision-card-body">
        <div class="vision-label"><i data-lucide="user-check" class="lucide"></i> この企業でのあなたの活躍像</div>
        <p class="vision-text">${getVision(c.visions, strengthIds)}</p>
      </div>
      <div class="vision-card-footer">
        <div class="vision-company-point"><i data-lucide="star" class="lucide"></i> ${c.point}</div>
        <a href="companies.html#company-${c.id}" class="support-link">企業詳細を見る <i data-lucide="arrow-right" class="lucide"></i></a>
      </div>
    </div>
  `;
  }).join('');
}

